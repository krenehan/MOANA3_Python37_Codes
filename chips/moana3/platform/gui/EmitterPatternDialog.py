    ################################
    # Disable check boxes that are outside of the specified number of patterns
    ################################
    def __disable_check_boxes(self):
        for pattern in range(16):
            pattern_index = pattern*self.__emitter_pattern.number_of_chips
            for chip in range(16):
                if pattern >= self.__emitter_pattern.patterns_per_frame:
                    flat_index = pattern_index + chip
                    self.__masterList[flat_index].setCheckable(False)
                    
    
    ################################
    # Master update function
    ################################
    def __update(self):
        
        # Update emitter pattern
        self.__update_emitter_pattern()
        
        # Update IR emitters
        self.__update_ir_emitters()
        
                    
    ################################
    # Update the emitter pattern structure
    ################################
    def __update_emitter_pattern(self):
        # print("update function called")
        
        # Loop through the check boxes and determine if they are checked
        for pattern in range(self.__emitter_pattern.patterns_per_frame):
            pattern_index = pattern * self.__emitter_pattern.number_of_chips
            for chip in range(self.__emitter_pattern.number_of_chips):
                flat_index = pattern_index + chip
                self.__emitter_pattern.emitter_pattern[pattern][chip] = self.__masterList[flat_index].isChecked()
                
        # Show the emitter pattern
        self.__show_emitter_pattern()

    ################################
    # Show the emitter pattern structure
    ################################
    def __show_emitter_pattern(self):
        
        # Loop through the check boxes and set them to be checked if the chip emitter is enabled during that pattern
        for pattern in range(self.__emitter_pattern.patterns_per_frame):
            pattern_index = pattern * self.__emitter_pattern.number_of_chips
            for chip in range(self.__emitter_pattern.number_of_chips):
                flat_index = pattern_index + chip
                if self.__emitter_pattern.emitter_pattern[pattern][chip]: 
                    self.__masterList[flat_index].setChecked(True)
                    
                    
    ################################
    # Update the IR emitters
    ################################
    def __update_ir_emitters(self):
        # print("update function called")
        
        # Loop through the check boxes and determine if they are checked
        for chip in range(self.__emitter_pattern.number_of_chips):
            self.__emitter_pattern.ir_emitters[chip] = self.__irList[chip].isChecked()
                
        # Show the emitter pattern
        self.__show_ir_emitters()

    ################################
    # Show the IR emitters
    ################################
    def __show_ir_emitters(self):
        
        # Loop through the check boxes and set them to be checked if the chip emitter is enabled during that pattern
        for chip in range(self.__emitter_pattern.number_of_chips):
            if self.__emitter_pattern.ir_emitters[chip]: 
                self.__irList[chip].setChecked(True)


